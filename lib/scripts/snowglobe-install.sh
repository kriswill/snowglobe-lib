_set_username() {
	unset USERNAME
	unset NORMAL_USER

	printf "Note: Username will be cleansed of characters that are not [a-z][A-Z][0-9], -, or _\n"
	while :; do
		read -rp "Username: " USERNAME
		# cleanse username of problematic characters
		USERNAME=$(printf "%s" "$USERNAME" | tr -d '[`"=$%#^<>+(){}*!&[]~|][:blank:][:cntrl:]')
		if [ -z "$USERNAME" ]; then
			printf "Username cannot be empty.\n"
			continue
		else
			break
		fi
	done
	if [ "$USERNAME" != "root" ]; then
		NORMAL_USER=true
	fi
}

_set_password() {
	unset PASSWORD
	unset PASSWORD2

	while [ -z "$PASSWORD" ] || [ "$PASSWORD" != "$PASSWORD2" ]; do
		read -rs -p "Enter Password: " PASSWORD
		printf "\n"
		if [ -z "$PASSWORD" ]; then
			printf "No password provided.\n"
			if [ -z "$1" ]; then
				break
			else
				continue
			fi
		fi
		read -rs -p "Confirm Password: " PASSWORD2
		printf "\n"
		if [ "$PASSWORD" != "$PASSWORD2" ]; then
			printf "\nPasswords do not match! Try again.\n"
			continue
		fi
	done
}

