done

CONFIG_ROOT="/mnt/etc/nixos"
HOST_CONFIG_DIR="$CONFIG_ROOT/nixosConfigurations/$HOSTNAME"
HOSTS_CONFIG_FILE="$CONFIG_ROOT/nixosConfigurations/default.nix"

y_or_n --msg="Use the default localization configuration? (en_US.UTF-8)" --default="yes" && LOCALE="en_US.UTF-8"
if [ -z "$LOCALE" ]; then
	LOCALE=$(cat /etc/locales.txt | fzf --disabled --border --border-label-pos 1:bottom --border-label="Select the default locale")
	if [ -z "$LOCALE" ]; then
		printf "\nNo localization selected, assuming default.\n"
		LOCALE="en_US.UTF-8"
	else
		LOCALE="$LOCALE"".UTF-8"
	fi
fi

printf "\nChosen Locale: %s\n" "$LOCALE"

y_or_n --msg="Use the default keyboard layout? (US)" --default="yes" && XKB_LAYOUT="us"
if [ -z "$XKB_LAYOUT" ]; then
	XKB_LAYOUT=$(localectl list-keymaps | fzf --disabled --border --border-label-pos 1:bottom --border-label="Select a keyboard layout")
	if [ -z "$XKB_LAYOUT" ]; then
		printf "\nNo layout selected, assuming default (US)\n"
		XKB_LAYOUT="us"
	fi
fi

printf "\nChosen xkb keymap: %s\n" "$XKB_LAYOUT"
printf "Note: This will only apply to window managers under X11 and the Linux Virtual Console.\n"
printf "For wayland compositors like niri or hyprland, you will have to set the keymap in their respective configuration files.\n\n"

y_or_n --msg="Install a desktop envrionment?" --default="yes" && {

	# TODO update this list as more desktops are supported
	KDE_DESCRIPTION="A modern all inclusive desktop environment for wayland.
Very similar to Microsoft Windows 11.
Perfect for beginners."

	NIRI_DESCRIPTION="A high-quality, modern DIY wayland window manager.
Uses infinite scrolling windows in a tiled format.
Recommended for enthusists / advanced users only."

	DESKTOPS="Niri KDE"

	SELECTED_DESKTOP=$(printf "%s\n" "$DESKTOPS" | tr ' ' '\n' | while read -r desktop; do
		printf "%s\n" "$desktop"
	done | fzf --disabled --border --border-label-pos 1:bottom --border-label='Select a desktop environment' --preview "
  if [ {} = 'Niri' ]; then
    printf '$NIRI_DESCRIPTION'
  elif [ {} = 'KDE' ]; then
    printf '$KDE_DESCRIPTION'
  fi
	" | tr '[:upper:]' '[:lower:]')
	if [ -z "$SELECTED_DESKTOP" ]; then
		printf "No desktop selected. Aborting\n"
		exit 1
	fi

	printf "\nDesktop: %s\n\n" "$SELECTED_DESKTOP"
}

printf "NixOS will install with the following configuration.\n"
printf "CPU Brand - %s\n" "$CPU_VENDOR"
if [ -n "$GPU_VENDORS" ]; then
	printf "GPU Brands - $(printf "%s\n" "$GPU_VENDORS" | tr ' ' '\n' | while read -r gpu; do
		printf "%s, " "$gpu"
	done)"
	printf "\n"
fi
printf "Firmware - %s\n" "$FIRMWARE"
printf "System - %s\n" "$SYSTEM_ARCH"
printf "Locale - %s\n" "$LOCALE"
printf "Keyboard - %s\n" "$XKB_LAYOUT"
if [ -n "$SELECTED_DESKTOP" ]; then
	printf "Desktop - %s\n" "$SELECTED_DESKTOP"
fi
printf "NixOS Version - %s\n\n" "$NIXOS_VERSION"

y_or_n --msg="If this is correct, begin writing config to $CONFIG_ROOT?" || {
	printf "Aborted\n"
	exit 1
}

