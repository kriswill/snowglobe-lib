fi

# detect if VM
if lscpu | grep 'Hypervisor vendor:' >/dev/null; then
	QEMU=true
else
	QEMU=false
fi

_check_gpu() {
	if lspci -nn | grep "VGA" | grep -i "$1" >/dev/null; then
		GPU_VENDORS="$GPU_VENDORS""$(printf '"%s" ' "$1")"
	fi
}

_check_gpu "nvidia"
_check_gpu "amd"
_check_gpu "intel"

SYSTEM_ARCH=$(lscpu | grep Arch | tr -d " " | cut -d: -f2)
NIXOS_VERSION=$(nixos-version | cut -d. -f1-2)

_format_disks

# begin writing configuration repository
while :; do
	printf "\nSet your system hostname: "
	read -r HOSTNAME
	# TODO validate hostname
	if [ -z "$HOSTNAME" ]; then
		printf "Hostname cannot be empty.\n"
