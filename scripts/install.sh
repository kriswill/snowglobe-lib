if [ -d "$CONFIG_ROOT" ]; then
	rm -rf "$CONFIG_ROOT"
fi
mkdir -p "$CONFIG_ROOT"

# if the configuration is new
if [ -z "$APPEND_MODE" ]; then
	# flake.nix
	printf " {
			description = \"my NixOS configurations\";

			inputs = {
				# Uncomment to lock your own nixpkgs revision in the flake.lock.
				# Could cause instabilities, use only if you know what you are doing.
				# nixpkgs = {
				#   url = \"github:NixOS/nixpkgs/nixos-unstable\";
				# };

				earthgman = {
					url = \"git+https://git.earthgman.dev/EarthGman/nix-modules\";
					# Be sure to also uncomment this if you use your own nixpkgs input. Mismatched system dependencies are not good.
					# inputs.nixpkgs.follows = \"nixpkgs\";
				};
			};

			outputs = { self, nixpkgs, earthgman, ... }@inputs:
			let
				lib = earthgman.lib; # nixpkgs.lib merged with some custom functions used for modules.
				outputs = self.outputs;
			in
			{
				# expose hosts configured under this flake
				nixosConfigurations = import ./nixosConfigurations { inherit lib inputs outputs; };

				# expose your custom modules.
				nixosModules.default = import ./nixosModules { inherit lib inputs outputs; };

				# expose your overlays (package set modifications)
				overlays = import ./overlays { inherit inputs; };

				# your custom derivations
				packages = 
				let
					supported-systems = [
						# add more system targets if you need.
						\"%s-linux\"
					];
				in
				# generate a package attribute set for each supported architecture
				lib.genAttrs supported-systems (
					system:
					import ./packages {
						pkgs = import nixpkgs {
							config.allowUnfree = true;
							inherit system;
							# give your packages access to your overlays
							overlays = builtins.attrValues outputs.overlays; # transform the set of overlays to a list
						};
					}
				);
			};
		}" "$SYSTEM_ARCH" >$CONFIG_ROOT/flake.nix

	# nixosModules/default.nix
	printf "
    # wrapper for your custom nixosModules
		{ inputs, outputs, lib, ... }:
		{
			pkgs,
			lib,
			config,
			...
		}:
		{
			# automatically wraps up module files present in this directory
			imports = lib.autoImport ./. { } ++ [
				# EXAMPLE
				# inputs.home-manager.nixosModules.default
			];

			# import your overlays to all hosts
			nixpkgs.overlays = builtins.attrValues outputs.overlays;

			# contain and enable your custom modules
			# If you wish to let your modules be consumable by other flakes, be sure to segment them to avoid conflicts.

			# options.your-name-here.enable = lib.mkEnableOption \"your-name-here's NixOS modules\";

			# apply only if your modules are enabled
			# config = lib.mkIf config.your-name-here.enable {
			#   your-name-here.module1.enable = true;
			#   your-name-here.module2.enable = true;
			# };
	}" | install -D /dev/stdin "$CONFIG_ROOT/nixosModules/default.nix"

	# nixosModules/core
	printf "
    # reserved for any modifications you wish to make to the core NixOS module tree from nixpkgs
		{ pkgs, lib, config, ... }:
		{
			imports = lib.autoImport ./. { };
		}" | install -D /dev/stdin "$CONFIG_ROOT/nixosModules/core/default.nix"

	# packages
	printf "
	  # create custom derivations using pkgs.callPackage
